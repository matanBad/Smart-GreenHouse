def log_rejected_reading(sensor_id, event_type, rejection_reason, original_payload=None):
    """
    Convenience helper to record a rejected sensor reading.

    `event_type` should be the most specific rejection reason code
    (e.g. "invalid_unit", "non_numeric_value"); `rejection_reason` is a
    human-readable summary of all problems found.
    """
    return add_log(
        event_type=event_type,
        description="Sensor reading rejected by validation",
        sensor_id=sensor_id,
        rejection_reason=rejection_reason,
        original_payload=original_payload,
    )


