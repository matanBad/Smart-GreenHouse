def log_unauthorized_access_attempt(role, action, endpoint):
    """
    Record that a valid role attempted an action it is not permitted to perform.

    The role, the attempted action, and the endpoint are kept in `details` so the
    security audit trail captures who tried to do what, and where.
    """
    return add_log(
        event_type="unauthorized_access_attempt",
        description=f"Unauthorized action '{action}' attempted by role '{role}'",
        details={"role": role, "attempted_action": action, "endpoint": endpoint},
    )


def log_invalid_role_attempt(role, action, endpoint):
    """Record a request that carried a missing or unrecognised user role."""
    return add_log(
        event_type="invalid_role_attempt",
        description=f"Request for action '{action}' had a missing or invalid role",
        details={"role": role, "attempted_action": action, "endpoint": endpoint},
    )


def get_logs():
    """Return all system log entries, newest first."""
    logs = read_json("system_logs.json", default=[])
    return list(reversed(logs))
