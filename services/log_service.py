"""
log_service.py -> System Logs

Future PRD area: System Logs.

Records system events into data/system_logs.json for auditing and debugging.
For Prompt 2 the key job is logging *rejected* sensor readings so there is an
audit trail of every reading the API refused (and why), without ever storing
the rejected reading in readings.json.
"""

import uuid
from datetime import datetime, timezone

from models.system_log_model import make_log
from services.storage_service import append_json, read_json


def _now_iso():
    """Current UTC time as an ISO-8601 string."""
    return datetime.now(timezone.utc).isoformat()


def _new_log_id():
    """Generate a unique log id."""
    return "LOG-" + uuid.uuid4().hex[:12]


def add_log(
    event_type,
    description,
    sensor_id=None,
    sensor_type=None,
    alert_id=None,
    actuator_id=None,
    rejection_reason=None,
    original_payload=None,
    details=None,
):
    """Create and persist a system log entry. Returns the stored entry."""
    entry = make_log(
        log_id=_new_log_id(),
        event_type=event_type,
        description=description,
        sensor_id=sensor_id,
        sensor_type=sensor_type,
        alert_id=alert_id,
        actuator_id=actuator_id,
        rejection_reason=rejection_reason,
        original_payload=original_payload,
        details=details,
        created_at=_now_iso(),
    )
    append_json("system_logs.json", entry)
    return entry


def log_alert_generated(alert_id, sensor_id, severity, alert_type):
    """Record that an alert was generated by the backend."""
    return add_log(
        event_type="alert_generated",
        description=f"{severity} alert generated ({alert_type})",
        sensor_id=sensor_id,
        alert_id=alert_id,
    )


