def log_threshold_rule_updated(sensor_type, old_rule, new_rule, role):
    """Record a successful threshold rule update (with old/new values)."""
    details = {
        "old_min_value": old_rule.get("min_value"),
        "old_max_value": old_rule.get("max_value"),
        "old_warning_margin": old_rule.get("warning_margin"),
        "new_min_value": new_rule.get("min_value"),
        "new_max_value": new_rule.get("max_value"),
        "new_warning_margin": new_rule.get("warning_margin"),
        "role": role,
    }
    return add_log(
        event_type="threshold_rule_updated",
        description=f"Threshold rule updated for {sensor_type}",
        sensor_type=sensor_type,
        details=details,
    )


def log_unauthorized_threshold_update(sensor_type, role):
    """Record a rejected threshold update due to an unauthorized role."""
    return add_log(
        event_type="unauthorized_threshold_update_attempt",
        description="Threshold update rejected: caller is not a system administrator",
        sensor_type=sensor_type,
        details={"role": role},
    )


def log_invalid_threshold_update(sensor_type, errors, role):
    """Record a rejected threshold update that failed validation."""
    return add_log(
        event_type="invalid_threshold_update_attempt",
        description="Threshold update rejected: invalid values",
        sensor_type=sensor_type,
        rejection_reason="; ".join(errors) if errors else None,
        details={"role": role, "errors": errors},
    )


